std::function<Level(const Resources & resources)> DefaultLevelMaker::makeFunctionCreatingNextDefaultLevel(LevelName levelName)
{
	int numberOfNextLevel = static_cast<int>(levelName) + 1;
	return makeFunctionCreatingDefaultLevel(static_cast<LevelName>(numberOfNextLevel));
}

std::function<Level(const Resources & resources)> DefaultLevelMaker::makeFunctionCreatingDefaultLevel(LevelName levelName)
{
	switch (levelName)
	{
	case LevelName::playerLevel:
		return std::function<Level(const Resources & resources)>();
		break;
	case LevelName::Level1:
		return makeFunctionCreatingLevel1();
		break;
	case LevelName::Level2:
		return makeFunctionCreatingLevel2();
		break;
	case LevelName::Level3:
		return makeFunctionCreatingLevel3();
		break;
	default:
		break;
	}
}

std::unique_ptr<Enemy> DefaultLevelMaker::createBoss(LevelName levelName, PlayerType playerType, const Resources& resources)
{
	switch (levelName)
	{
	case LevelName::Level1:
		return std::make_unique<DeadlyFlowerBoss>(sf::Vector2f(3501.22593, 500.000000), resources);
	case LevelName::Level2:
		return std::make_unique<SkeletonBoss>(sf::Vector2f(3501.22593, 500.000000), resources);
	case LevelName::Level3:
		return std::make_unique<ZombieBoss>(sf::Vector2f(3501.22593, 500.000000), resources, (playerType == PlayerType::stormtrooper ? true : false));
	default:
		break;
	}
}

